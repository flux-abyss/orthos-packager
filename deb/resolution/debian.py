# Post-build artifact validation
# ---------------------------------------------------------------------------

def _dpkg_deb_field(deb_path: str, field: str) -> str:
    """Return the value of *field* from a .deb binary control block.

    Uses 'dpkg-deb -f' which reads the binary control file embedded in the
    archive without extracting it.  Returns an empty string on any error.
    """
    try:
        result = subprocess.run(
            ["dpkg-deb", "-f", deb_path, field],
            capture_output=True,
            text=True,
            timeout=15,
            check=False,
        )
    except (OSError, subprocess.TimeoutExpired):
        return ""
    return result.stdout.strip() if result.returncode == 0 else ""


def _parse_depends_field(depends: str) -> list[list[str]]:
    """Parse a Debian Depends field into a list of alternative groups.

    Each element in the returned list is a list of bare package names
    representing one OR-group (alternatives separated by '|').  Version
    constraints and architecture qualifiers are stripped so only the bare
    name remains for oracle validation.

    Example::

        "libc6 (>= 2.38), libefl (>= 1.26.99) | libother, python3"
        →  [["libc6"], ["libefl", "libother"], ["python3"]]

    Empty or whitespace-only fields return an empty list.
    """
    if not depends.strip():
        return []

    groups: list[list[str]] = []
    for group_str in depends.split(","):
        group_str = group_str.strip()
        if not group_str:
            continue
        alternatives: list[str] = []
        for alt in group_str.split("|"):
            bare = alt.strip().split("(")[0].strip()
            if bare:
                alternatives.append(bare)
        if alternatives:
            groups.append(alternatives)
    return groups


def validate_built_debs(
    deb_paths: list[str],
    generated_pkg_names: frozenset[str],
    oracle: AptOracle | None = None,
) -> None:
    """Inspect every .deb in *deb_paths* and fail if any Depends are invalid.

    This is the final artifact gate.  It catches dependency contamination
    that bypasses the pre-build 'debian/control' validation — most commonly
    upstream library names injected by 'dh_shlibdeps' through
    '${shlibs:Depends}' when the build host has non-Debian shlibs metadata.

    The oracle determines which apt database is authoritative:
    * :class:`~deb.resolution.oracle.ChrootAptOracle` — uses the target chroot's
      database; the correct choice when building in a Debian chroot.
    * :class:`~deb.resolution.oracle.HostAptOracle` — uses the host's database;
      may miss invalid deps that exist on the host but not in the target archive.

    Algorithm
    ---------
    For each .deb:

    1. Read the 'Package' and 'Depends' fields via 'dpkg-deb -f'.
    2. Parse 'Depends' into OR-groups (alternatives separated by '|').
    3. For each group, check whether at least one alternative is:
       - A debhelper substitution variable.
       - A generated sibling package (in *generated_pkg_names*).
       - A package the oracle confirms as installable.
    4. If any group has no valid alternative, collect the failure and
       enrich it with shlibs-repair diagnostics.

    Args:
        deb_paths: Absolute paths to the .deb files to validate.
            Non-.deb paths (e.g. .changes, .buildinfo) are silently skipped.
        generated_pkg_names: Frozenset of binary package names produced by
            this run.  Used to exempt sibling deps from oracle validation.
        oracle: The apt oracle to use.  Defaults to
            :class:`~deb.resolution.oracle.HostAptOracle` (with a warning) if
            'None'.

    Raises:
        RuntimeError: When one or more .deb files contain invalid dependencies.
    """
    if oracle is None:
        oracle = make_oracle(None)

    failures: list[str] = []

    for deb_path in deb_paths:
        if not deb_path.endswith(".deb"):
            continue

        pkg_name = _dpkg_deb_field(deb_path, "Package")
        depends_raw = _dpkg_deb_field(deb_path, "Depends")

        if not pkg_name:
            # Unreadable .deb — skip rather than abort; build failure is
            # already surfaced via dpkg-buildpackage's exit code.
            continue

        if not depends_raw:
            # No Depends field — nothing to validate.
            continue

        groups = _parse_depends_field(depends_raw)
        bad_groups: list[str] = []

        for alternatives in groups:
            if not _group_is_valid(alternatives, generated_pkg_names, oracle):
                bad_groups.append(" | ".join(alternatives))

        if bad_groups:
            # Collect bare names that failed for repair-diagnostic lookup.
            # A bad group may be "libefl | libother" — check each alternative.
            bad_bare: list[str] = []
            for grp in bad_groups:
                for alt in grp.split(" | "):
                    alt = alt.strip()
                    if alt and not alt.startswith("${") and alt not in bad_bare:
                        bad_bare.append(alt)

            repair_results = repair_shlibs_deps(bad_bare)
            repair_map = {r.bad_dep: r for r in repair_results}

            for bad in bad_groups:
                repair_lines: list[str] = []
                for alt in bad.split(" | "):
                    alt = alt.strip()
                    if alt in repair_map:
                        repair_lines.append(
                            repair_map[alt].format_diagnostic()
                        )

                failures.append(
                    f"  deb:     {deb_path}\n"
                    f"  package: {pkg_name}\n"
                    f"  invalid: {bad!r}\n"
                    f"  depends: {depends_raw}\n"
                    f"  oracle:  {oracle!r}\n"
                    f"  cause:   likely shlibs/substvars contamination "
                    f"(host has non-Debian library metadata)\n"
                    + ("\n".join(repair_lines) + "\n" if repair_lines else "")
                )

    if failures:
        lines = [
            "artifact dependency validation failed — built .deb(s) contain "
            "unresolvable dependencies:"
        ]
        for f in failures:
            lines.append("")
            lines.append(f.rstrip())
        raise RuntimeError("\n".join(lines))
