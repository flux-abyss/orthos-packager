"""Debian resolution Layer — runtime dependency validation.

Every explicitly emitted runtime dependency must correspond to a real,
installable Debian package.  This module is the single enforcement point
that prevents upstream logical names (e.g. 'libefl') from leaking into
'debian/control'.

Design constraints
------------------
* No project-specific rules.  Detection is purely name-based via apt.
* Deterministic: the :class:`~deb.resolution.oracle.AptOracle` is the sole
  source of truth for package existence.
* ELF-dynamic deps are NOT passed here; they are already suppressed by the
  'elf-dynamic' provenance filter in 'debian_generator._non_elf_runtime_deps'
  and delegated to '${shlibs:Depends}'.  Only non-ELF inferred deps reach
  this layer.
* All validation functions accept an explicit 'oracle' parameter so that
  callers can supply a :class:`~deb.resolution.oracle.ChrootAptOracle` when
  building inside a clean Debian chroot.  Without an oracle the functions
  default to :class:`~deb.resolution.oracle.HostAptOracle`, which may be
  contaminated by non-Debian packages on the build host.

Public API
----------
'resolve_runtime_dependencies(deps, oracle)'
    Given a list of candidate runtime package names, return only those
    confirmed to exist in the oracle's apt database.  Unresolvable names
    are dropped with a logged warning.

'validate_extra_depends(extra_depends, generated_pkg_names, pkg_label, oracle)'
    Validate one package's 'extra_depends' list, preserving generated
    intra-package deps (sibling packages, lockstep version pins) and
    debhelper substitution variables, and verifying everything else via
    the oracle.

'validate_built_debs(deb_paths, generated_pkg_names, oracle)'
    Inspect every retained .deb and raise 'RuntimeError' if any Depends
    group is invalid according to the oracle.  Enrich failures with
    shlibs-repair diagnostics.

'check_apt_package(name, oracle)'
    Low-level: return True when *name* is a known apt candidate in *oracle*.
"""

from __future__ import annotations

import subprocess

from deb.resolution.oracle import AptOracle, HostAptOracle, make_oracle
from deb.resolution.shlibs_repair import repair_shlibs_deps
from deb.utils.log import error, info


# ---------------------------------------------------------------------------
# Low-level apt oracle helper
# ---------------------------------------------------------------------------

def check_apt_package(
    name: str,
    oracle: AptOracle | None = None,
) -> bool:
    """Return True when *name* is a real, apt-installable Debian package.

    Delegates to *oracle*.package_exists().  If *oracle* is 'None', a
    :class:`~deb.resolution.oracle.HostAptOracle` is used (with an implicit
    contamination warning emitted by :func:`~deb.resolution.oracle.make_oracle`).

    Args:
        name: A Debian package name, optionally with a version constraint.
        oracle: The apt oracle to query.  Pass 'None' only when no target
                environment is available (host-scoped fallback).

    Returns:
        True if the package has a real candidate version in the oracle's
        apt database.
    """
    if oracle is None:
        oracle = make_oracle(None)
    return oracle.package_exists(name)


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _is_generated_dep(dep: str, generated_pkg_names: frozenset[str]) -> bool:
    """Return True for deps that are internally generated and need no apt check.

    Three categories are exempt from apt validation:

    1. Debhelper substitution variables — '${shlibs:Depends}',
       '${misc:Depends}', etc.  These are resolved at build time by
       debhelper and are always valid in 'debian/control'.

    2. Same-binary-version sibling deps — any dep of the form
       '<name> (= ${binary:Version})' where the bare name is one of the
       packages generated by this very run.  These are intra-package deps
       synthesised by 'synthesize_intra_deps' and 'dev_pkg_main_dep';
       they reference packages that do not exist in the apt database yet.

    3. Runtime-shlib sibling deps — when the runtime shlib package
       (e.g. '<app>-runtime') appears in 'generated_pkg_names' and the
       dep bare name matches it (with or without a version qualifier).  This
       is a subset of rule 2 but is covered by the same logic.
    """
    bare = dep.split("(")[0].strip()

    # 1. Debhelper substitution variable or empty bare name.
    if not bare or bare.startswith("${"):
        return True

    # 2 + 3. Bare name is a package we are generating in this run.
    if bare in generated_pkg_names:
        return True

    return False


def _group_is_valid(
    alternatives: list[str],
    generated_pkg_names: frozenset[str],
    oracle: AptOracle,
) -> bool:
    """Return True when at least one alternative in the group is acceptable.

    An alternative is acceptable when it is:
    * A debhelper substitution variable ('${...}').
    * A generated sibling package from this build (in *generated_pkg_names*).
    * A real apt package confirmed by *oracle*.
    """
    for alt in alternatives:
        if not alt or alt.startswith("${"):
            return True
        if alt in generated_pkg_names:
            return True
        if oracle.package_exists(alt):
            return True
    return False


