# Public API
# ---------------------------------------------------------------------------

def validate_extra_depends(
    extra_depends: list[str],
    generated_pkg_names: frozenset[str],
    pkg_label: str = "",
    oracle: AptOracle | None = None,
) -> list[str]:
    """Validate one package's 'extra_depends' against the apt database.

    Passes through without querying apt:

    * Debhelper substitution variables ('${...}').
    * Dependencies whose bare name is in *generated_pkg_names* — these are
      sibling packages produced by this packaging run and are not in the apt
      database yet.  This covers both intra-package sibling pulls
      ('<app>-data (= ${binary:Version})') and dev-to-main lockstep pins
      ('<app> (= ${binary:Version})').

    All other entries are validated via *oracle*.  Entries that fail
    validation are dropped with an 'error:' log line.

    Args:
        extra_depends: The list of dep strings from 'pkg["extra_depends"]'.
        generated_pkg_names: Frozenset of all binary package names produced by
            '_build_package_layout' for this run.
        pkg_label: Human-readable label for the package (used in log messages).
        oracle: The apt oracle to use.  Defaults to
            :class:`~deb.resolution.oracle.HostAptOracle` (with a warning) if
            'None'.

    Returns:
        Filtered list in the same order as *extra_depends*.
    """
    if oracle is None:
        oracle = make_oracle(None)

    kept: list[str] = []
    dropped: list[str] = []

    for dep in extra_depends:
        if _is_generated_dep(dep, generated_pkg_names):
            kept.append(dep)
            continue
        if oracle.package_exists(dep):
            kept.append(dep)
        else:
            dropped.append(dep)

    if dropped:
        prefix = f"[{pkg_label}] " if pkg_label else ""
        for name in dropped:
            error(
                f"resolution-layer: {prefix}dropped '{name}' — not found in apt; "
                "not a valid Debian package name (upstream logical name?)"
            )

    return kept


def validate_build_depends_str(
    build_depends_str: str,
    oracle: AptOracle,
) -> str:
    """Validate and filter a Build-Depends string against an AptOracle.

    - Preserves debhelper/control syntax entries (e.g. debhelper-compat).
    - Preserves version constraints.
    - Attempts lib<name>-dev -> lib<name>-all-dev fallback.
    - Drops completely unresolvable entries.
    """
    if not build_depends_str.strip():
        return ""

    kept_groups: list[str] = []
    
    groups = [g.strip() for g in build_depends_str.split(",") if g.strip()]
    for group in groups:
        alternatives = [a.strip() for a in group.split("|") if a.strip()]
        
        valid_alternatives: list[str] = []
        for alt in alternatives:
            bare = alt.split("(")[0].strip()
            
            if not bare or bare.startswith("${") or bare in ("debhelper", "debhelper-compat"):
                valid_alternatives.append(alt)
                continue
                
            if oracle.package_exists(bare):
                valid_alternatives.append(alt)
                continue
                
            if bare.startswith("lib") and bare.endswith("-dev"):
                base_name = bare[3:-4]
                fallback_bare = f"lib{base_name}-all-dev"
                if oracle.package_exists(fallback_bare):
                    version_part = alt[len(bare):]
                    valid_alternatives.append(f"{fallback_bare}{version_part}")
                    continue
            
            error(f"resolution-layer: Build-Depends dropped '{bare}' — not found in {oracle.__class__.__name__}")

        if valid_alternatives:
            kept_groups.append(" | ".join(valid_alternatives))

    return ", ".join(kept_groups)


def resolve_runtime_dependencies(
    deps: list[str],
    oracle: AptOracle | None = None,
) -> list[str]:
    """Validate *deps* against the apt database; return only real packages.

    Algorithm
    ---------
    For each candidate dependency name:

    1. If it is a debhelper substitution variable ('${...}'), pass it through
       unconditionally — debhelper resolves these at build time and they are
       always valid in 'debian/control'.

    2. Query *oracle*.  If the package has a real candidate version, keep it.

    3. Otherwise, log a warning and drop it.  Dropping is safe because:

       * ELF runtime deps are already delegated to '${shlibs:Depends}'.
       * Any remaining dep that the oracle cannot locate is either a logical
         upstream name (e.g. 'libefl') or a stale/mistyped name — emitting
         it would only break installation on the target system.

    Args:
        deps: List of candidate package names as produced by
              '_non_elf_runtime_deps' in 'debian_generator'.
        oracle: The apt oracle to use.  Defaults to
            :class:`~deb.resolution.oracle.HostAptOracle` (with a warning) if
            'None'.

    Returns:
        Filtered list in the same order, containing only oracle-verified names.

    Logging
    -------
    * 'resolved: <name>'   — package confirmed by oracle.
    * 'dropped:  <name>'   — package not found; removed from Depends.
    """
    if oracle is None:
        oracle = make_oracle(None)

    resolved: list[str] = []
    dropped: list[str] = []

    for dep in deps:
        if oracle.package_exists(dep):
            resolved.append(dep)
        else:
            dropped.append(dep)

    if resolved:
        info("runtime-resolution: resolved deps: " + ", ".join(resolved))
    if dropped:
        for name in dropped:
            error(
                f"runtime-resolution: dropped '{name}' — not found in apt; "
                "not a valid Debian package name (upstream logical name?)"
            )

    return resolved


